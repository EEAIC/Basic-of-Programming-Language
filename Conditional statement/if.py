
people = 3
apple = 20

# 'if'는 if 문의 시작
# <if 뒤에는 조건식을 쓰고>
if people < apple / 5:  # 조건식이 끝난면 :을 쓰고
    # <탭>키를 이용해 네 칸을 들여 쓴 다음
    print('신나는 사과 파티! 배 터지게 먹자!')   # 실행하고 싶은 코드!
if apple > people > apple / 5:
    print('사과수가 많지 않아! 몇 개는 쪼개 먹자!')
if people > apple:
    print('사람이 너무 많아! 몇 명은 ...')
